results = ["Mario", "Luigi"]

results.append("Peach") # Add Peach to the end of the list. Append is used to add a single item to the list.

results.extend(["Bowser", "Toad"]) # Add Bowser and Toad to the end of the list. Extend is used to add multiple items to the list.

results.insert(0, "Wario") # Add Wario to the beginning of the list. Insert is used to add an item at a specific index.



print(results)